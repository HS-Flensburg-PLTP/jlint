class UsePostIncrementDecrement {
    static int PreIncrementOperator(int i) {
        return ++i;
    }

    static int PostIncrementOperator(int i) {
        return i++;
    }

    static int PreDecrementOperator(int i) {
        return --i;
    }

    static int PostDecrementOperator(int i) {
        return i--;
    }
}